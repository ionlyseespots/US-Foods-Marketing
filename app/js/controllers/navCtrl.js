'use strict';

four51.app.controller('NavCtrl', function ($location, $route, $scope, $451, User) {
    $scope.Logout = function(){
        User.logout();
        if ($scope.isAnon) {
            $location.path("/catalog");
            User.login();
        }
    };

	$scope.refreshUser = function() {
		store.clear();
	}

    // http://stackoverflow.com/questions/12592472/how-to-highlight-a-current-menu-item-in-angularjs
    $scope.isActive = function(path) {
        var cur_path = $location.path().replace('/', '');
        var result = false;

        if (path instanceof Array) {
            angular.forEach(path, function(p) {
                if (p == cur_path && !result)
                    result = true;
            });
        }
        else {
            if (cur_path == path)
                result = true;
        }
        return result;
    };

	$scope.Clear = function() {
		localStorage.clear();
		$route.reload();
	}

	$scope.$on('event:orderUpdate', function(event, order) {
		$scope.cartCount = order ? order.Status != 'Unsubmitted' ? null : order.LineItems.length : null;
		$scope.subTotal = order ? order.Status != 'Unsubmitted' ? null : order.Subtotal : null;
	});
});